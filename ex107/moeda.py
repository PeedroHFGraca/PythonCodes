def aumentar(n, taxa=0): # A 'taxa' é Parâmetro opcional
    res = n + (n * taxa/100)
    return res


def dimunuir(n, taxa=0): # A 'taxa' é Parâmetro opcional
    res = n - (n * taxa/100)
    return res


def dobro(n):
    res = n * 2
    return res


def metade(n):
    res = n / 2
    return res


